import timeit

# 创建元组比创建列表快10倍

lb_time = timeit.timeit('[1,2,3,4,5,6,7,8,9,0]', number=999999)
print("创建999999个列表耗时：",lb_time) #输出结果： 创建999999个列表耗时： 0.09591219999856548


yz_time = timeit.timeit('(1,2,3,4,5,6,7,8,9,0)', number=999999)
print("创建999999个元组耗时：",yz_time) #输出结果：创建999999个元组耗时： 0.012144300002546515
